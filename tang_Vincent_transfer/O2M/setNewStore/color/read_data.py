with open('source_model_training_record50epoch50_0.pkl.txt', 'r') as f:
    lines = f.readlines()
    accuracy = ''
    for line in lines:
        if 'fruit' in line:
            fruit = line.split('\n')[0]
            print(fruit)
            if accuracy != '':
                print(accuracy)
                with open('D:\\Tang\\source_model_training_record_short_.txt', 'a') as ff:
                    ff.write(accuracy)
            print(line)
            with open('D:\\Tang\\source_model_training_record_short_.txt', 'a') as ff:
                ff.write(line)
        if 'layer' in line:
            print(line)
            with open('D:\\Tang\\source_model_training_record_short_.txt', 'a') as ff:
                ff.write(line)
        accuracy = line
    with open('D:\\Tang\\source_model_training_record_short_.txt', 'a') as ff:
        ff.write(line)
